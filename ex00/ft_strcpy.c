#include <stdio.h>

char *ft_strcpy(char *dest, char *src);

{ 
	int i;

	i = 0;
	while(src[i] != '\0')
	{
		dest[i] = src[i];
		 i = i+1;
	}
	dest[1] = '\0';
	return(dest);
}
